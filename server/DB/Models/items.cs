class Item
{
    constructor(ItemID, Title, Information, Category, Skill, Rarity, Cost)
    {
        this.ItemID = ItemID;
        this.Title = Title;
        this.Information = Information;
        this.Category = Category;
        this.Skill = Skill;
        this.Rarity = Rarity;
        this.Cost = Cost;
    }
}
